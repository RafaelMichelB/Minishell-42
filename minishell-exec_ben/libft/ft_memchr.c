#include "libft.h"

void	*ft_memchr(const void *pt, int c, size_t n)
{
	while (n > 0)
	{
		if (*(char *)pt == (char)c)
			return ((void *)pt);
		pt++;
		n--;
	}
	return (NULL);
}
